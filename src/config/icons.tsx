// Icon component for easy usage
export const Icon = ({
  name,
  ...props
}: IconProps & {
  name: IconName;
}) => {
  const IconComponent = icons[name];

  if (!IconComponent) {
    console.warn(`Icon "${name}" does not exist in the icon library.`);
    return null;
  }

  return <IconComponent {...props} />;
};

